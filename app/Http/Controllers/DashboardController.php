    {
        $request->validate([
            'title' =>'required',
            'breif' => 'required|max:255',
            'body' => 'required',
            'image' => 'required|image|mimes:png,jpg,jpeg,gif,bmp',
        ]);
        $post = new Post ; 
        $post-> create($request->all());

        return back();
    }

